# Contoh operator aritmatika di Python
a = 10
b = 3

print("a + b =", a + b)    # 13
print("a - b =", a - b)    # 7
print("a * b =", a * b)    # 30
print("a / b =", a / b)    # 3.3333333333333335
print("a // b =", a // b)  # 3
print("a % b =", a % b)    # 1
print("a ** b =", a ** b)  # 1000

# Unary operators
print("-a =", -a)          # -10
print("+a =", +a)          # 10
